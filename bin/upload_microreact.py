#!/usr/bin/env python3
"""Post sonnei-typer results table to Microreact via the v2 API."""

import argparse
import csv
import datetime
import json
import os
import sys
import time

try:
    import requests
except ImportError:
    sys.exit("ERROR: requests library not installed. Run: pip install requests")


MICROREACT_API = "https://microreact.org/api/projects/create"


def read_and_rename(tsv_path):
    """Read TSV and rename 'sample' column to 'id' (required by Microreact)."""
    with open(tsv_path) as fh:
        content = fh.read()
    # Microreact requires the row-key column to be named 'id'
    return content.replace("sample\t", "id\t", 1)


def get_columns(tsv_path):
    """Return list of column names (with 'sample' renamed to 'id')."""
    with open(tsv_path) as fh:
        reader = csv.DictReader(fh, delimiter="\t")
        cols = reader.fieldnames or []
    return ["id" if c == "sample" else c for c in cols]


def upload(tsv_path, project_name, token):
    blob = read_and_rename(tsv_path)
    columns = get_columns(tsv_path)
    timestamp = datetime.datetime.now().strftime("%Y-%m-%d %H:%M")

    payload = {
        "version": 1,
        "schema": "https://microreact.org/schema/v1.json",
        "meta": {
            "name": f"{project_name} — {timestamp}",
            "description": "Auto-generated by sonnei-typer pipeline",
        },
        "files": {
            "data-file-1": {
                "id":     "data-file-1",
                "name":   "sonnei_typer_results.tsv",
                "format": "text/tab-separated-values",
                "blob":   blob,
            }
        },
        "datasets": {
            "dataset-1": {
                "file":        "data-file-1",
                "idFieldName": "id",
            }
        },
        "tables": {
            "table-1": {
                "dataset": "dataset-1",
                "file":    "data-file-1",
                "title":   "Genotyping results",
                "columns": [{"field": c} for c in columns],
            }
        },
    }

    headers = {
        "Access-Token": token,
        "Content-Type": "application/json; charset=utf-8",
    }

    time.sleep(1)

    resp = requests.post(MICROREACT_API, headers=headers,
                         data=json.dumps(payload), timeout=60)

    if resp.status_code in (200, 201):
        result = resp.json()
        url = result.get("url") or f"https://microreact.org/project/{result.get('id', 'unknown')}"
        return url
    else:
        raise RuntimeError(
            f"Microreact API returned {resp.status_code}: {resp.text[:500]}"
        )


def main():
    parser = argparse.ArgumentParser(description="Upload results to Microreact")
    parser.add_argument("--input",   required=True,  help="sonnei_typer_results.tsv")
    parser.add_argument("--project", required=True,  help="Project name")
    parser.add_argument("--token",   required=False, default=None,
                        help="Microreact API token (or set MICROREACT_TOKEN env var)")
    parser.add_argument("--output",  required=True,  help="File to write project URL to")
    args = parser.parse_args()

    token = args.token or os.environ.get("MICROREACT_TOKEN")
    if not token:
        sys.exit("ERROR: Microreact API token not provided. "
                 "Use --token or set the MICROREACT_TOKEN environment variable. "
                 "Run: nextflow secrets set MICROREACT_TOKEN <your_token>")

    print(f"Uploading {args.input} to Microreact...", file=sys.stderr)

    try:
        url = upload(args.input, args.project, token)
    except Exception as e:
        with open(args.output, "w") as fh:
            fh.write(f"UPLOAD_FAILED: {e}\n")
        sys.exit(f"ERROR: Microreact upload failed: {e}")

    with open(args.output, "w") as fh:
        fh.write(url + "\n")

    print(f"Microreact project URL: {url}", file=sys.stderr)


if __name__ == "__main__":
    main()
